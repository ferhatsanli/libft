#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*ptr;

	if (!s)
		return ;
	ptr = s;
	i = 0;
	while (i < n)
	{
		*ptr++ = 0;
	}
}
