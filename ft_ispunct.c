int	ft_ispunct(int c)
{
	if (c >= '!' && c <= '/')
		return (1);
	else if (c > '9' && c < 'A')
		return (1);
	else if (c > 'Z' && c < 'a')
		return (1);
	else if (c > 'z' && c <= '~')
		return (1);
	else
		return (0);
}
